<script>
    document.querySelectorAll(".group").forEach(row => {
        const preview = row.querySelector(".service-preview");
        if (!preview) return;
        const img = preview.querySelector(".preview-img");

        row.addEventListener("mouseenter", () => {
            img.src = row.dataset.image;
            preview.style.opacity = "1";
            img.classList.add("reveal");
        });

        row.addEventListener("mouseleave", () => {
            img.classList.remove("reveal");
            preview.style.opacity = "0";

        });
    });

    const text = "View All Project • View All Services •";
    const container = document.getElementById("circular-text");
    const radius = 90;
    const degStep = 360 / text.length;

    text.split("").forEach((char, i) => {
        const span = document.createElement("span");
        span.innerText = char;
        span.style.position = "absolute";
        span.style.left = "50%";
        span.style.top = "50%";
        span.style.transform = `rotate(${i * degStep}deg) translate(${radius}px) rotate(${-i * degStep}deg)`;
        span.style.transformOrigin = "0 0";
        container.appendChild(span);
    });
</script>